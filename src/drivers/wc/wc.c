uint8_t wcIsConnectedToI2C(void)
{
	return (0 == i2cPeripheralTestAddressPresence(I2C_0_MASTER_NUM, WC_IIC_ADDR, 1));
}

void wcGetStatus(uint8_t* st0, uint8_t* st1)
{
	gpioExpanderReadWCST(st0, st1);
}

uint8_t wcReadAndVerifyConfiguration(void)
{
	uint8_t valid = 1;

	wcRead256BytesConfigurationToChip(readBuf);

	for (int i = 0; i < READ_BUF_LENGTH ; i++)
	{
		if (readBuf[i] != wc_configuration_b[i])
		{
			valid = 0;
			break;
		}
	}

	return valid;
}

void wcWrite256BytesConfigurationToChip(void)
{
	i2cPeripheralMasterTakeMutex(I2C_0_MASTER_NUM);

	for (int i = 0; i < 256; i++)
	{
		i2cPeripheralMasterWriteByte(I2C_0_MASTER_NUM, WC_IIC_ADDR, i, wc_configuration_b[i], 0);
		vTaskDelay(10 / portTICK_RATE_MS);
	}

	i2cPeripheralMasterGiveMutex(I2C_0_MASTER_NUM);
}

void wcRead256BytesConfigurationToChip(uint8_t* readBuffer)
{
	memset(writeBuf, 0, 16);

	writeBuf[0] = 0;

	i2cPeripheralMasterWriteReadDevice(I2C_0_MASTER_NUM, WC_IIC_ADDR, writeBuf, 1,
			readBuffer, 256, 2000 / portTICK_RATE_MS, 1);
}
